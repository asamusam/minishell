int	ft_isspace(int c)
{
	return (c == 32 || (9 <= c && c <= 13));
}

int	is_word(char c)
{
	if (c == '|' || c == '<' || c == '>')
		return (0);
	if (c == '\'' || c == '\"')
		return (0);
	if (ft_isspace(c) || !c)
		return (0);
	return (1);
}